    <p class="p2"><b>NAME:</b>Jelly Fish<br><br><b>SCIENTIFIC NAME:</b>Scyphozoa<br><br><b>ORIGIN:</b>America and Antarctica<br><br><b>LIFESPAN:</b>2 to 3 Years</p>
    <p class="p3">Turritopsis dohrnii, also known as the immortal jellyfish, is a species of small, biologically immortal jellyfish[2][3] found worldwide in temperate to tropic waters. It is one of the few known cases of animals capable of reverting completely to a sexually immature, colonial stage after having reached sexual maturity as a solitary individual. Others include the jellyfish Laodicea undulata[4] and species of the genus Aurelia.[5]</p>
    <p class="p4">Theoretically, this process can go on indefinitely, effectively rendering the jellyfish biologically immortal,[3][8] although in practice individuals can still die. In nature, most Turritopsis dohrnii are likely to succumb to predation or disease in the medusa stage without reverting to the polyp form.[9]

        The capability of biological immortality with no maximum lifespan makes T. dohrnii an important target of basic biological, aging and pharmaceutical research.[10]</p>
    <p class="p5">Like most other hydrozoans, T. dohrnii begin their life as tiny, free-swimming larvae known as planulae. As a planula settles down, it gives rise to a colony of polyps that are attached to the sea floor. All the polyps and jellyfish arising from a single planula are genetically identical clones.[6] The polyps form into an extensively branched form, which is not commonly seen in most jellyfish. Jellyfish, also known as medusae, then bud off these polyps and continue their life in a free-swimming form, eventually becoming sexually mature. When sexually mature, they have been known to prey on other jellyfish species at a rapid pace. If the T. dohrnii jellyfish is exposed to environmental stress, physical assault, or is sick or old, it can revert to the polyp stage, forming a new polyp colony.[7] It does this through the cell development process of transdifferentiation, which alters the differentiated state of the cells and transforms them into new types of cells.</p>
    
