    <p class="p3">Monitor lizards are lizards in the genus Varanus, the only extant genus in the family Varanidae. They are native to Africa, Asia, and Oceania, and one species is also found in the Americas as an invasive species. About 80 species are recognized.</p>
    <p class="p4">Monitor lizards have long necks, powerful tails and claws, and well-developed limbs. The adult length of extant species ranges from 20 cm (7.9 in) in some species, to over 3 m (10 ft) in the case of the Komodo dragon, though the extinct varanid known as megalania (Varanus priscus) may have been capable of reaching lengths more than 7 m (23 ft).</p>
    <p class="p5">Monitor lizards have a high aerobic scope[13][14] that is afforded, in part, by their heart anatomy. Whereas most reptiles are considered to have three-chambered hearts, the hearts of monitor lizards – as with those of boas and pythons – have a well developed ventricular septum that completely separates the pulmonary and systemic sides of the circulatory system during systole.[15] This allows monitor lizards to create mammalian-equivalent pressure differentials between the pulmonary and systemic circuits,[15] which in turn ensure that oxygenated blood is quickly distributed to the body without also flooding the lungs with high-pressure blood.</p>

