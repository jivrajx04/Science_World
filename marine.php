    <p class="p4">Tortoises can vary in size with some species, such as the Galápagos giant tortoise, growing to more than 1.2 metres (3.9 ft) in length, whereas others like the Speckled cape tortoise have shells that measure only 6.8 centimetres (2.7 in) long.[1] Several lineages of tortoises have independently evolved very large body sizes in excess of 100 kg, including the Galapagos giant tortoise and the Aldabra giant tortoise.</p>
