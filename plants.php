    <p class="p4">Worldwide, there are several hundred cultivars of mango. Depending on the cultivar, mango fruit varies in size, shape, sweetness, skin color, and flesh color which may be pale yellow, gold, green, or orange.[1] Mango is the national fruit of India, Pakistan and the Philippines,[5][6] while the mango tree is the national tree of Bangladesh.[7]</p>
    <p class="p5">In deep soil, the taproot descends to a depth of 6 m (20 ft), with profuse, wide-spreading feeder roots and anchor roots penetrating deeply into the soil.[1] The leaves are evergreen, alternate, simple, 15–35 centimetres (6–14 inches) long, and 6–16 cm (2+1⁄2–6+1⁄2 in) broad; when the leaves are young they are orange-pink, rapidly changing to a dark, glossy red, then dark green as they mature.</p>
    <p class="p6">The flowers are produced in terminal panicles 10–40 cm (4–15+1⁄2 in) long; each flower is small and white with five petals 5–10 millimetres (3⁄16–3⁄8 in) long, with a mild, sweet fragrance.[1] Over 500 varieties of mangoes are known,[1] many of which ripen in summer, while some give a double crop.[11] The fruit takes four to five months from flowering to ripen.[1]</p>
