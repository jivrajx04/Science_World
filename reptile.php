    <p class="p3">The king cobra (Ophiophagus hannah) is a species of venomous elapid snake endemic to jungles in Southern and Southeast Asia. The sole member of the genus Ophiophagus, it is distinguishable from other cobras, most noticeably by its size and neck patterns. </p>
         <p class="p4"> The king cobra is the world's longest venomous snake, with an average length of 3.18 to 4 m (10.4 to 13.1 ft),[2] reaching a maximum of 5.85 m (19.2 ft).[3] Its skin colour varies across the habitats, from black with white stripes to unbroken brownish grey. It preys chiefly on other snakes, including its own species. Unlike other snakes, it rarely hunts other vertebrates, such as rodents and lizards.</p>
    <p class="p5">Like most cobras and mambas, the king cobra's threat display includes spreading its neck-flap, raising its head upright, puffing, and hissing. Despite its fearsome reputation, the king cobra avoids confrontation with humans whenever possible. When provoked, however, it is capable of striking a target at long range and well above the ground. Rather than biting and retreating, it may sustain its bite and inject a large quantity of venom, which is a medical emergency.[4][5]</p>
    
