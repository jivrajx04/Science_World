    <p class="p6">The slender, leaning, ringed trunk of the coconut palm rises to a height of up to 25 metres (80 feet) from a swollen base and is surmounted by a graceful crown of giant featherlike leaves. Mature fruits, ovoid or ellipsoid in shape, 300–450 mm (12–18 inches) in length and 150–200 mm (6–8 inches) in diameter, have a thick fibrous husk surrounding the familiar single-seeded nut of commerce. A hard shell encloses the insignificant embryo with its abundant endosperm, composed of both meat and liquid. Coconut fruits float readily and have been dispersed widely by ocean currents and by humans throughout the tropics.</p>
