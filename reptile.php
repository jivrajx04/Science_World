    <p class="p3">Craspedocephalus gramineus, known as the bamboo pit viper, Indian green pit viper, or common green pit viper,[3] is a venomous pit viper species found in the southern and north eastern parts of India. No subspecies are currently recognized.</p>
    <p class="p4">The upper parts are usually bright green, rarely yellowish, greyish, or purplish brown, with or without black, brown, or reddish spots; usually a light, white, yellow, or red streak along the outer row of scales; end of tail frequently yellow or red; lower parts green, yellow, or whitish.[4]</p>
    <p class="p5">The rostral scale is as deep as broad or broader than deep. The upper head-scales are small, smooth, imbricate; supraocular scale narrow, rarely broken up. The internasals are contact or separated by one or two scales. There are 8 to 13 scales on a line between the supraoculars; usually one or two, rarely three, series of scales between the suboculars and the labials; 9 to 12 upper labials, second usually forming the anterior border of the loreal pit, third largest; temporal scales smooth. The dorsal scales are more or less distinctly keeled, in 21 (rarely 19 or 23) rows; ventrals 145–175; anal scale entire; subcaudals in two rows 53–76.</p>
    
