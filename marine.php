    <p class="p2"><b>NAME:</b>Whale<br><br><b>SCIENTIFIC NAME:</b>Cetacea<br><br><b>ORIGIN:</b>Asia and Antarctica<br><br><b>LIFESPAN:</b>85 to 90 Years</p>
    <p class="p3">The bowhead whale (Balaena mysticetus) is a species of baleen whale belonging to the family Balaenidae and the only living representative of the genus Balaena. They are the only baleen whale endemic to the Arctic and subarctic waters, and are named after their characteristic massive triangular skull, which they use to break through Arctic ice. Other common names of the species are the Greenland right whale or Arctic whale. </p>
    <p class="p4">Bowheads have the largest mouth of any animal[6] representing almost one-third of the length of the body, the longest baleen plates with a maximum length of 4 metres (13 feet)[7] and may be the longest-lived mammals, with the ability to reach an age of more than 200 years.[8]

        The bowhead was an early whaling target. Their population was severely reduced before a 1966 moratorium was passed to protect the species. Of the five stocks of bowhead populations, three are listed as "endangered", one as "vulnerable", and one as "lower risk, conservation dependent" according to the IUCN Red List. The global population is assessed as of least concern.[3][9]</p>
    <p class="p5">Today, the bowhead whale occupies a monotypic genus, separate from the right whales, as proposed by the work of John Edward Gray in 1821.[11] For the next 180 years, the family Balaenidae was the subject of great taxonometric debate. Authorities have repeatedly recategorized the three populations of right whale plus the bowhead whale, as one, two, three or four species, either in a single genus or in two separate genera. Eventually, it was recognized that bowheads and right whales were different, but there was still no strong consensus as to whether they shared a single genus or two. As recently as 1998, Dale Rice, in his comprehensive and otherwise authoritative classification, Marine Mammals of the World: Systematics and Distribution, listed just two species: B. glacialis (the right whales) and B. mysticetus (the bowheads).[12]

