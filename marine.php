    <p class="p2"><b>NAME:</b>Shark<br><br><b>SCIENTIFIC NAME:</b>Selachimorpha<br><br><b>ORIGIN:</b>Asia and Antarctica<br><br><b>LIFESPAN:</b>20 to 30 Years</p>
    <p class="p3">The Greenland shark (Somniosus microcephalus), also known as the gurry shark, grey shark, or by the Kalaallisut name eqalussuaq, is a large shark of the family Somniosidae ("sleeper sharks"), closely related to the Pacific and southern sleeper sharks.[2] The distribution of this species is mostly restricted to the waters of the North Atlantic Ocean and Arctic Ocean; however, more recent evidence indicates that at great depths it may range as far south as the Caribbean.</p>
    <p class="p4">The Greenland shark has the longest known lifespan of all vertebrate species (estimated to be between 250 and 500 years),[3] and is among the largest extant species of shark. It is a generalist feeder, consuming a variety of available foods.[4] As an adaptation to living at depth,[5] it has a high concentration of trimethylamine N-oxide in its tissues, which causes the meat to be toxic.[6] Greenland shark flesh, treated to reduce toxin levels, is eaten in Iceland as a delicacy known as kæstur hákarl.[7] </p>
    <p class="p5">The Greenland shark is one of the largest living species of shark. It usually grows between 2.4–4.3 m (7.9–14.1 ft) long and weighs between 700–1,000 kg (1,500–2,200 lb),[8][9] but has been recorded at up to 7.3 m (24 ft) and more than 1,400 kg (3,100 lb).[10][11] Most Greenland sharks observed have been around 2.4–4.8 m (8–16 ft) long and weigh up to 400 kg (880 lb).[10][11]

        Males are typically smaller than females. It rivals the Pacific sleeper shark (possibly up to 7 m or 23 ft long) as the largest species in the family Somniosidae. The Greenland shark is a thickset species, with a short, rounded snout, small eyes, and very small dorsal and pectoral fins. The gill openings are very small for the species' great size.
        
        Coloration can range from pale creamy-gray to blackish-brown and the body is typically uniform in color, though whitish spots or faint dark streaks are occasionally seen on the back.[10]</p>
    
